<div class="bordeaux ph" style="display:none;">波尔多是是法国葡萄酒的骄傲，尤以红酒在全世界最为有名，口感柔顺细雅,是一种非常优雅的葡萄酒,是"法国葡萄酒王后"<br/>
波尔多的葡萄酒绝大多数是在以家庭为单位的庄园里生产的，庄园里有葡萄园，有酒窖。葡萄的生长、收获，葡萄酒的发酵、成熟及装瓶的全过程都在庄园里完成。在波尔多，13000个种植者经营着113000公顷的葡萄园，分为57个独立的AOC(原产地监控命名)区；每年出 产8.5亿瓶葡萄酒，其中40%用于出口，占法国葡萄酒出口总量的25%。<br/>
波尔多产区的气候和地理条件得天独厚:它临大西洋,气候温和,土壤形态多,有吉伦特河流过,葡萄树在这生长最佳。纪隆德河，加龙河，多尔多涅河这三条河流将波尔多分成了左岸，右岸，中部地区三部分。
波尔多的法定葡萄品种有赤霞珠，梅乐，品丽珠，小维尔多和马尔贝克。<br/>
波尔多著名的酒庄有：拉菲，拉图，玛歌，奥比昂，木桐（这五大酒庄位于加龙河的左岸）；奥松，白马，柏翠（这三大酒庄位于右岸）<br/><br/><br/></div>
